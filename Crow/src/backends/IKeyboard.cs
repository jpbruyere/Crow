using System;

namespace Crow
{
	public interface IKeyboard
	{
		event EventHandler<KeyEventArgs> KeyDown;
		event EventHandler<KeyEventArgs> KeyUp;
		event EventHandler<KeyPressEventArgs> KeyPress;


		void HandleEvent (uint Keycode, bool pressed);

		bool IsDown (Key key);
		bool Shift { get; }
		bool Ctrl { get; }
		bool Alt { get; }

		void Destroy ();
	}
}

